package as;

public class Addition {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 7;
        System.out.println(num1 + " + " + num2 + "= " + num1 + num2);
        System.out.println(num1 + " - " + num2 + "= " + (num1 - num2));
        System.out.println(num1 + " * " + num2 + "= " + num1 * num2);
        System.out.println(num1 + " / " + num2 + "= " + num1 / num2);
        System.out.println(num1 + " % " + num2 + "= " + num1 % num2);

    }


}
